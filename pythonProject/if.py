# if문

people = 3
apple = 20

# if뒤의 조건식이 충족될 때 프린트됨
if people < apple / 5:  # 3 < 20/5 = true
    print('신나는 사과 파티! 배 터지게 먹자!')

if apple % people > 0:  # 20 % 3 > 0 = true
    print('사과 수가 맞지 않아! 몇 개는 쪼개 먹자!')

if people > apple:  # 3 > 20 = false
    print('사람이 너무 많아! 몇 명은...')

if True:
    print('조건문이 True니까 실행됨')

if False:
    print('조건문이 False니까 실행안됨')




