# 연산식
addtion = 100 + 2 # 덧셈
substraction = 100 - 2  # 뺄셈
multiplication = 3 * 2  # 곱셈
quotient = 10 / 2   # 몫(나눗셈)
squared = 2 ** 10 # 제곱 / 거듭제곱
remainder = 15 % 4  # 나머지(나눗셈)
print('100 + 2 = ', addtion)
print('100 - 2 = ', substraction)
print('3 * 2 = ', multiplication)
print('10 / 2 = ', quotient)
print('2 ** 10 = ', quotient)
print('15 % 4 = ', remainder)

